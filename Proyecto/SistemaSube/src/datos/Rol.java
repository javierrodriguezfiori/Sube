package datos;

public class Rol {
	private int idRol;
	private String descripcion;
	
	public Rol() { }

	public int getIdRol() {
		return idRol;
	}

	private void setIdRol(int idRol) {
		this.idRol = idRol;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
